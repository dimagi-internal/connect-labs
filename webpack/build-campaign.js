/**
 * Campaign Utility Tool — build step.
 *
 * The campaign app (commcare_connect/static/campaign/) is a set of NON-module
 * scripts that share a single global scope across a specific load order: each
 * file's top-level `function`/`const` (e.g. `Empty`, `Page`, `CUTC` in
 * primitives.jsx, `BOOTSTRAP`/`CURRENT_USER` in app.jsx) is a global the later
 * files reference. They use the GLOBAL `React`/`ReactDOM` (classic JSX runtime),
 * never `import`/`export`.
 *
 * Historically these were transpiled IN THE BROWSER via `@babel/standalone`
 * (~2.8 MB from a CDN) on every page load. This script moves that JSX→JS
 * transpile to BUILD time: it babel-transpiles each file with the *classic*
 * preset-react runtime (so JSX compiles to `React.createElement(...)`, keeping
 * the global-`React` contract), then concatenates the results IN ORDER into one
 * bundle that executes in a single shared scope — semantically identical to the
 * old ordered `<script>` tags, just pre-transpiled and with no CDN Babel.
 *
 * Output: commcare_connect/static/bundles/js/campaign-bundle.js
 *
 * NOTE: we intentionally do NOT use webpack here. Webpack wraps every module in
 * its own function scope, which would break the global-sharing contract these
 * files depend on. A plain ordered concat is the correct, lowest-risk shape.
 */
const fs = require('fs');
const path = require('path');
const babel = require('@babel/core');

const SRC_DIR = path.resolve(__dirname, '../commcare_connect/static/campaign');
const OUT_DIR = path.resolve(
  __dirname,
  '../commcare_connect/static/bundles/js',
);
const OUT_FILE = path.join(OUT_DIR, 'campaign-bundle.js');

// EXACT load order — must match the old <script> tags in
// templates/campaign/app.html. app.jsx MUST be last (it mounts the root).
const FILES = [
  'perms.js',
  'primitives.jsx',
  'shell.jsx',
  'data-api.js',
  'workers-actions.js',
  'tab_overview.jsx',
  'tab_workers.jsx',
  'tab_workers_kyc.jsx',
  'tab_workers_profile.jsx',
  'tab_planning_detail.jsx',
  'tab_planning.jsx',
  'tab_activity.jsx',
  'tab_reporting.jsx',
  'tab_users.jsx',
  'tab_connections.jsx',
  'tab_training.jsx',
  'app.jsx',
];

function transpile(file) {
  const filename = path.join(SRC_DIR, file);
  const code = fs.readFileSync(filename, 'utf8');
  const result = babel.transformSync(code, {
    filename,
    // No project .babelrc / package.json "babel" — we pin the CLASSIC react
    // runtime here on purpose. The repo's default preset config uses
    // runtime:"automatic", which injects `import { jsx } from
    // "react/jsx-runtime"` and would break the non-module global-scope contract.
    babelrc: false,
    configFile: false,
    presets: [['@babel/preset-react', { runtime: 'classic' }]],
    // Keep it ES2017+; browsers targeted here support it and these files are
    // already written in that style. We only need JSX stripped, not down-level.
    compact: false,
    comments: false,
  });
  return result.code;
}

function build() {
  if (!fs.existsSync(OUT_DIR)) {
    fs.mkdirSync(OUT_DIR, { recursive: true });
  }

  const parts = [
    '/* Campaign Utility Tool — built bundle. DO NOT EDIT.',
    ' * Generated by webpack/build-campaign.js from static/campaign/*.{jsx,js}.',
    ' * Concatenated in load order; runs in one shared global scope. */',
    '',
  ];

  for (const file of FILES) {
    parts.push(`/* ===== ${file} ===== */`);
    parts.push(transpile(file));
    parts.push('');
  }

  fs.writeFileSync(OUT_FILE, parts.join('\n'), 'utf8');
  const kb = (fs.statSync(OUT_FILE).size / 1024).toFixed(1);
  console.log(`[campaign] built ${OUT_FILE} (${FILES.length} files, ${kb} KB)`);
}

build();

if (process.argv.includes('--watch')) {
  console.log('[campaign] watching for changes…');
  let timer = null;
  const rebuild = () => {
    clearTimeout(timer);
    timer = setTimeout(() => {
      try {
        build();
      } catch (e) {
        console.error('[campaign] build failed:', e.message);
      }
    }, 100);
  };
  for (const file of FILES) {
    fs.watch(path.join(SRC_DIR, file), rebuild);
  }
}
